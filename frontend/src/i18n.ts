export function normalizeLanguage(language: string | undefined | null): AppLanguage {
  if (language?.toLowerCase().startsWith("en")) return "en-US";
  if (language?.toLowerCase().startsWith("zh")) return "zh-CN";
  return defaultLanguage;
}

export function nextLanguage(language: string | undefined | null): AppLanguage {
  const normalized = normalizeLanguage(language);
  const currentIndex = supportedLanguages.indexOf(normalized);
  return supportedLanguages[(currentIndex + 1) % supportedLanguages.length];
}

export function tText(tree: TranslationTree, dottedKey: string): string | undefined {
  const value = dottedKey.split(".").reduce<string | TranslationTree | undefined>((current, key) => {
    if (current && typeof current === "object") return current[key];
    return undefined;
  }, tree);
  return typeof value === "string" ? value : undefined;
}

export function initI18n(): typeof i18n {
  if (i18n.isInitialized) return i18n;
  void i18n
    .use(LanguageDetector)
    .use(initReactI18next)
    .init({
      resources: {
        "zh-CN": { translation: resources["zh-CN"] },
        "en-US": { translation: resources["en-US"] }
      },
      fallbackLng: defaultLanguage,
      supportedLngs: [...supportedLanguages],
      load: "currentOnly",
      interpolation: { escapeValue: false },
      detection: {
        order: ["localStorage", "navigator"],
        caches: ["localStorage"],
        lookupLocalStorage: "ttsMoreLanguage",
        convertDetectedLanguage: normalizeLanguage
      }
    });
  return i18n;
}
